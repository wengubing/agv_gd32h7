/***************************************************************************************
** 函数名称: calc_sum16
** 功能描述: 计算16位ChkSum,原始参数ADD_SUM为0x5a,XOR_SUM为0xa5
** 参    数: p		/ 数据指针
** 参    数: n		/ 数据长度
** 返 回 值: 返回高8位为ADD_SUM,低8位为XOR_SUM    
***************************************************************************************/
u16 calc_sum16(u8 *p,u16 n)
{
	u16 i;
	u8  j;
	u8  add_sum;
	u8  xor_sum;

	add_sum = 0x5a;				// 和的初始值
	xor_sum = 0xa5;				// 异或的初始值

	for(i = 0; i < n; i++)		// 循环运算
	{
		j = *p++;
		add_sum += j;			// 和
		xor_sum ^= j;			// 异或
	}
	return(((u16)add_sum << 8 ) | xor_sum);
} 			  

