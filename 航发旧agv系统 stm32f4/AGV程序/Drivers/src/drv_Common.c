/***************************************************************************************
** 函数名称: s16_ave
** 功能描述: 计算16位有符号数的平均值
** 参    数: val	/ 新值
** 参    数: p		/ 缓冲区地址
** 参    数: pe		/ 指针地址
** 参    数: n		/ 缓冲区元件个数(不是字节数)
** 返 回 值: 返回值经过4舍5入 
***************************************************************************************/
s16 s16_ave(s16 val, s16 *p, u16 *pe, u16 n)
{
	u16 i;
	s32 j = 0;

	i = *pe;										
	*(p + i) = val;
	if(++(*pe) >= n)
	{
		*pe = 0;
	}
	for(i = 0; i < n; i++)
	{
		j += *p++;
	}
	i = n >> 1;
	if(j > 0)
	{
		j += i;
	}
	else
	{
		j -= i;
	}
	j /= n;
	return (j);
}	
