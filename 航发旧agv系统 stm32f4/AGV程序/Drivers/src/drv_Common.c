u16 crc16(u8 *ptr, u16 len) 
{
	u16 crc;
	u8 da;
	crc = 0;
	while(len-- != 0) 
	{
		da = (u8)(crc >> 8);		//以8位二进制数的形式暂存CRC的高8位
		crc <<= 8;					//左移8位，相当于CRC的低8位乘以
		crc ^= crc_ta[da^*ptr];		//高8位和当前字节相加后再查表求CRC ，再加上以前的CRC
		ptr++;
	}
	return(crc);
}

