s32 GtoB(u32 lValue)
{
	register u32 lV = lValue, lMax = 1;
	while(lV > 0)
	{
		lV   >>= 1;
		lMax <<= 1;
	}
	if(lMax == 0) 
	{
		return -1;
	}
	return g2b(0, --lMax, lValue);
}

