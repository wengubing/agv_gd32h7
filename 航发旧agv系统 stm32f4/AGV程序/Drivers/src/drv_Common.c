/***************************************************************************************
** 函数名称: sqrt_u16
** 功能描述: 开根号处理 
** 参    数: M		/ 32位源数据
** 返 回 值: 16位平方根
***************************************************************************************/
u16 sqrt_u16(u32 M) 
{ 
	u16 N, i; 
	u32 tmp, ttp;              // 结果、循环计数 
	if (M == 0)                // 被开方数，开方结果也为0 
	{
		return 0; 
	}

	N = 0; 

	tmp = (M >> 30);           // 获取最高位：B[m-1] 
	M <<= 2; 
	if (tmp > 1)               // 最高位为1 
	{ 
		N ++;                  // 结果当前位为1，否则为默认的0 
		tmp -= N; 
	} 

	for (i=15; i>0; i--)       // 求剩余的15位 
	{ 
		N <<= 1;               // 左移一位 

		tmp <<= 2; 
		tmp += (M >> 30);      // 假设 

		ttp = N; 
		ttp = (ttp<<1)+1; 

		M <<= 2; 
		if (tmp >= ttp)        // 假设成立 
		{ 
			tmp -= ttp; 
			N ++; 
		} 
	} 

	return N; 
} 

/***************************************************************************************
** 函数名称: sqrt_f32
** 功能描述: 浮点开方
** 参    数: num	/ 32位浮点数
** 返 回 值: 32位浮点数
***************************************************************************************/
fp32 sqrt_f32(fp32 number) 
{ 
	s32   i; 
	fp32  x, y; 
	const fp32 f = 1.5F; 
	
	x = number * 0.5F; 
	y = number; 
	i = *(s32*)&y; 
	i = 0x5f3759df - (i >> 1);  // 卡马克 
//	i = 0x5f375a86 - (i >> 1);  // Lomont 
	y = *(fp32*)&i; 
	y = y * (f - (x * y * y)); 
	y = y * (f - (x * y * y)); 
	return (number * y); 
} 		   			   

/***************************************************************************************
** 函数名称: f32toi32
** 功能描述: 32位浮点数转换为32位有符号整数
** 参    数: in	/ 32位浮点数
** 返 回 值: 32位有符号整数 
***************************************************************************************/
s32 f32toi32(fp32 in)
{
	u32 da;
	s32 out;
	u8  s;
	u8  e;

	da = cu32p(&in);					// 转换为二进制

	s   = (da >> 31);					// 符号值
	e   = (23 + 127) - (da >> 23);		// 指数值
	da  = (da & 0x7FFFFF) | 0x800000;	// 还原数据
	out = (da >>  e);					// 移出小数

	if(s)
	{
		out = -out;	
	}

	return out;
}
	   
