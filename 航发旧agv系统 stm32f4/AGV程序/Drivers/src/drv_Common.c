/***************************************************************************************
* 名称：Modbus_CRC()
* 功能：Modbus的CRC16校验
* 入口参数1：puchMsg 校验的数据
* 入口参数2：usDataLen 数据的长度
* 出口参数 ：CRC16结果
* --------------------------------------------------------------------------------------
* 备    注 ：小端模式,对结果进行反向以适应Modbus的要求
***************************************************************************************/
u16 Modbus_CRC(u8 *puchMsg, u32 usDataLen)
{
	u8 uchCRCHi = 0xFF ;					// 高CRC字节初始化
	u8 uchCRCLo = 0xFF ;					// 低CRC字节初始化
	u8 uIndex ;								// CRC循环中的索引 

	do
	{
		uIndex = uchCRCHi ^ *puchMsg;		// 计算CRC
		puchMsg++;
		uchCRCHi = uchCRCLo ^ auchCRCHi[uIndex] ;
		uchCRCLo = auchCRCLo[uIndex] ;
	}while(--usDataLen!=0);					// 传输消息缓冲区
	return (uchCRCLo << 8 | uchCRCHi);
}	  
			 
