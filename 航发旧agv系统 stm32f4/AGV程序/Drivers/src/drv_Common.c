/***************************************************************************************
** 函数名称: Complement_to_Former_Code
** 功能描述: 将负数的补码转换成原码
** 参    数: 
							x_data:输入的实体型负数
** 返 回 值: 	o_data : x_data的原码   
***************************************************************************************/
s8 Cmt_to_Frm_Code(s8 x_data)
{
	u8 i;
	s8	o_data = 0;
	for(i = 0; i < 7; i ++)
	{//将负数补码形式的二进制数值位按位取反,从最低位开始取.
		if(x_data & 0x01)
		{//当前位"1"
			o_data |= (0 << i);//对当前位取反	
		}
		else
		{
			o_data |= (1 << i);
		}
		x_data >>= 1;
	}
	o_data += 1;//最低位+1
	o_data |= 0x80;//置最高位符号位"1",即为负数
	return(o_data);
}

